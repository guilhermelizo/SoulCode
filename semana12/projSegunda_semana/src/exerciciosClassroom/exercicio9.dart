//   O Null safety veio da nova atualização do Dart, onde as variáveis agora 
//  são 'non-nullables', o que significa que elas não podem ser nulas. 
//  Caso seja necessário criar uma váriável nula, é necessário utilizar o '?',
//  criando então a váriável pelo seguinte exemplo: "String? nome". 
//  o '?' fará com que seja possível a variável 'nome' iniciar nula. 
//  o '?? 0' - serve como um teste, caso a variável esteja nula então seu valor será
//  mostrado como 0 (não precisa ser 0, depende do que for passado),
//  se não estiver nula, mostrará então seu valor atribuido.
//  o '!' serve para dizer que ali com certeza terá um valor. 
